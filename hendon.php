<h3>This month</h3>
    
    
<p>The Home Office recognises the importance of learning and development. Senior leaders in the Department wish to promote a culture where learning is valued and recognised as one of the most important tools for our development, both as individuals and as an organisation. With this in mind, we are pleased to introduce you to our new journey, 'learning to learn'.</p>
    
<strong>What does this mean for learners?</strong>
<p>Many of us in the Home Office regularly undertake more than the annual five learning and development days that are encouraged by the Civil Service. However, much of our learning will focus on mandatory training requirements or upskilling activities, such as the workshops on the new performance management system. While these are important learning activities, there is a variety of other ways you can learn and develop individually and as a team.</p>

<strong>How are you prioritising your team’s learning?</strong>
<p>Fitting learning into our day jobs can feel pressured and out of reach, especially when business delivery takes precedence. There is often a strong emphasis on doing ‘what’s expected of me’ rather than ‘continually building my skills and experience’ to become a professional expert in my field. Striking the balance between the ‘day job’ versus learning and development is essential to improve our professional capability. Research has shown that learning together through experience, also known as social learning, is recommended as a way that can help us to learn more effectively.</p>

<strong>What is social learning?</strong>
    <img src="/img/img1a.png" alt="image shows a sliding scale of learning, from the event, to the momentum, and then the intergration into the workplace">
    
<p>Social learning is not often recognised as “learning” as it is something we do as part of our everyday roles, for example, having discussions with colleagues, reading interesting articles, or catching up on LinkedIn. We learn from each other via observation, imitation and modelling. The challenge for us in the workplace is to make social learning more intentional and effective.</p>

    <img src="/img/img2a.jpg" alt="image shows the 70, 20, 10 learning model. 70 percent on the job experience, 20 percent mentoring and 10 percent classroom courses">
    
    <p>Social learning forms part of the ‘learning from experience’ (70%) and ‘learning from others’ (20%) sections of the 70:20:10 Model for Learning and Development, which is a commonly used formula to describe the optimal sources of professional learning. For information about what this means in practice, we encourage you to watch the short video below.</p>
    
    
