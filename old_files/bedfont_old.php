    <strong><h4 class="space-space">The suggested roundtable discussion topic is this month’s Let’s Talk About topic - Creating an environment where it is safe to challenge</h4></strong>
    <h3>Background</h3>
  <p>'Let’s talk about…’ gives you and your team an opportunity to get involved in how we shape our business, share good practice and make your voice heard. Each issue of Let’s talk about will be sponsored by a leader in Capabilities &amp; Resources (C&amp;R).</p>

<p>This issue is sponsored by Rowena Fenning from the C&amp;R Delivery team, and is about creating an environment where it is safe to challenge.</p>
 
    <h3>This Month</h3>
<p>Each year the People Survey asks us the extent to which we agree with the statement ‘I think it is safe to challenge the way things are done in the Home Office’. In 2016, 38% of respondents in C&amp;R agreed or strongly agreed, while 30% disagreed or strongly disagreed.</p>

<p>Creating an environment where more of us feel that it is safe to challenge is a key priority for the C&amp;R SMT this year and supports us in embedding our principles. Whatever the nature of the challenge – be it around behaviours or the handling of a piece of work – it is important for the wellbeing of the organisation as much as for individuals that people can give and receive challenge in a constructive way.</p>

<p>This month, we would like teams to have a discussion about making C&amp;R a place where it is increasingly safe to challenge. Your feedback will inform a programme of work to better understand the issues and actively promote positive change.</p>
    
    <h3>Suggested Questions</h3>

    <p>Please take time to discuss the following questions at your next team meeting:</p>

<ul>
    <li>When deciding if you agree with the statement ‘I think it is safe to challenge the way things are done in the Home Office’, what sort of thing do you think of challenging? [e.g. Local processes or ways of handling a piece of work? Departmental processes?Unacceptable behaviour? Misconduct? A breach of the civil service code? More than one of these? Something else?]</li> 
<li>What are the most important factors in giving you confidence to challenge something? [e.g. Your relationship with your line manager and your team? Access to an independent process? Regular consultation or opportunities to provide structured feedback? Reassurance about how your challenge will be dealt with? Something else?]</li>
<li>Do you have any specific suggestions for ways we can make Capabilities &amp; Resources a place where it is increasingly safe to challenge?</li>
    </ul>
    
    
    <h3>Feedback</h3>
    <p>After this and any other team discussions, please submit your feedback to these 3 questions to Rowena via this <a href="http://www.smartcdn.co.uk/homeofficeR4/mailresponse.asp?tid=10570&em=3903619&turl=http://www.homeofficesurveys.homeoffice.gov.uk/s/0ODN7/">survey link</a> – (this should take no more than 5 minutes) by Friday 26 May.</p>
    

