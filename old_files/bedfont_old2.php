   <strong><h3 class="space-space">Creating an environment where it is safe to challenge</h3></strong>
    <h3>What you talked about</h3>
  <p>Last month we asked you to have a discussion in your teams about creating an environment where it feels safe to challenge.  We wanted your views to inform a programme of work we are undertaking to actively promote positive change across Capabilities &amp; Resources (C&amp;R). </p>

<p>The questions generated considerable discussion among many of you and I’m grateful that you took the time to feedback the varied views in your teams.  There are diverse contexts and experiences which contribute to everyone’s opinions around whether it is safe to challenge. Your feedback indicates that challenging local processes/ways of working and unacceptable behaviour are the things usually uppermost in your mind.  But you also talked about a wider range of challenges, or questions – from performance, applying our principles and imposed change, to failure to comply with procedure or serious misconduct. 

<p>It was great to find that many of you feel you can raise constructive questions around the way you work, and to challenge behaviours in your team.  Where this is happening you say open and trusting working relationships in the team are important.  Some of you said that it is easier to challenge in your immediate team and it becomes more difficult to safely challenge more remote senior people or departmental processes.  Challenging processes is more comfortable for many of you than challenging behaviours.  </p>
    
<p>However, it is concerning to hear that a number of you don’t feel that it is safe to challenge in your immediate work environment, or at all. Overall, the balance between the importance of the issue and perceived personal risk (to day-to-day treatment, job or career options, or financial circumstances) makes a difference and, for some challenges, moral courage was identified as important.  You cited managers being defensive and taking challenge personally as an issue, and said you feel some managers have their own agenda and stifle alternative views.  Some of you in roles that involve challenging operational business areas felt this could present difficulties and more support was needed from internal managers in these instances.   </p>

<p>Overall you gave plenty of suggestions about C&amp;R.  You said you’d like to see senior managers setting an example, openly and visibly recognising challenge as positive and evidence of your trust, without attaching blame or negativity to individuals.  You want to see evidence of successful challenge shared and more use of local initiatives, such as anonymous suggestion boxes and team charters. </p>

<p>You said that the way challenge is made is as important as how it is received, and we need to get more used to doing this.  You felt opportunities, such as the engagement around the performance management process, should be increased, and the use of peer reviews in our work mainstreamed.  Mike Parsons’ regular dial-in was identified as a good way for staff to raise issues.  You suggested that we could offer some dedicated support to help staff who wish to make a challenge identify the best way of doing so, through resilience training and perhaps a buddying system, champion network or a specific contact point.</p>

<p>The Delivery Team is in the process of designing a toolkit with practical suggestions for implementing positive change across C&amp;R.  As part of this work, we will look at options for implementing your suggestions, and share greater detail around some of the ideas which C&amp;R colleagues have already tried and tested. We plan to deliver the toolkit towards the end of the summer.  In the mean time, thank you again to everyone who took part in the discussions and submitted thoughts and suggestions. </p>
    
<p>Rowena Fenning, Capabilities &amp; Resources Delivery Team </p>

    
