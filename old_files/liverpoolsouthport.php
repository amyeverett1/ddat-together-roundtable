   <strong><h3 class="space-space">Making Corporate Services a Great Place to Work</h3></strong>
    <h3>Background</h3>
  <p>Each edition of our manager briefing, ‘Let’s talk about’, focuses on an issue which is important to Capabilities &amp; Resources (C&amp;R) and on which we would like you to feed back your team’s thoughts.

This issue is sponsored by Mike Parsons, Director General for Capabilities and Resources and LGBT Champion, and Director of Talent, Leadership and Inclusion Jane Nicholson, and is about Creating an Inclusive Workplace.

</p>

<h3>This month</h3>
<p>We understand that some of our inclusivity policies and changes might not seem relevant to everyone, but it’s important to remember we all experience moments when we feel left out from a group and excluded.</p>

<p>For many, inclusivity and protected characteristics are seen as interchangeable terms, but supporting our staff networks is only part of being a truly inclusive employer. We are all unique and it’s essential we strive to create an environment where everyone feels they can bring their true selves to work.</p>

<p>The introduction of some gender neutral toilets into parts of 2 Marsham Street from the autumn is the latest example of how we are helping to make the Home Office a truly inclusive place to work.</p>

<p>We’re proud of the essential role our networks play in supporting and advising the Department on issues that affect our colleagues, and this latest move will give everyone the flexibility to use the space that makes them feel most comfortable.</p>

<p>So with the summer holidays drawing to a close, we would like to move into the autumn reflecting on what more we can do in the Home Office to make it a truly inclusive place to work.</p>

<p>In this month’s ‘Let’s talk about’, we’d like you to discuss what’s been your experience of inclusion in the Home Office and tell us what we should know as we plan our next steps.</p>

<h3>Suggested questions</h3>
    <ul>
    
    <li>When have you felt excluded at work? Why do you think it happened?</li>
    <li>What would help you to do your bit to create an inclusive environment at work?</li>
    <li>What more can we do to make C&amp;R a place where everybody feels included and free to be themselves?</li>
    </ul>
    
