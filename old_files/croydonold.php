   <strong><h3 class="space-space">Health, Safety and Wellbeing</h3></strong>
    <h3>Background</h3>
  <p>‘Let’s talk about…’ is a team briefing tool which is distributed to grades SEO and above for discussing with their teams. Each month we will highlight a specific topic for you to discuss. The topic will be an issue which is important to Capabilities &amp; Resources (C&amp;R) and on which we would like you to feed back your team’s thoughts.	‘Let’s talk about…’ gives you and your team an opportunity to get involved in how we shape our business, share good practice and make your voice heard. Each issue of Let’s talk about will be sponsored by a leader in C&amp;R. This issue is sponsored by Richard Palmer, the Head of the Health and Safety Team, and is about Health, Safety and Wellbeing.</p>

<h3>This month</h3>
<p>The Grenfell Tower tragedy has put health and safety at the front of everyone’s mind. But when we think about health, safety and wellbeing we rarely see it as a part of everything we do – it’s often perceived as an add-on and, in many cases, an interference.</p>
    
<p>Experience shows us that when incidents do occur people rarely change their behaviours but carry on doing what they always do. A fire in a casino in America had tragic consequences when rather than escape through the fire exits, people instead queued to collect their coats and cash in their chips.</p>
    
<p>When you go on holiday or stay in a hotel, what do you do? After going to our room and unpacking, most of us will check out the facilities - we’ll find the restaurant, bar and pool. Within half an hour we know our way around. But how many of us look at the emergency procedures? If there was an emergency, would we know where the fire exits and extinguishers were? If health, safety and wellbeing interventions are to be effective, they need to be a part of what we do every day in the Home Office. They need to be clear, relevant, sensible and proportionate to the risks. If they are part of an emergency plan, then they must be practised regularly enough to be effective.</p>
    
<p>In this month’s Let’s Talk About, which is sponsored by Richard Palmer, Head of the Health and Safety Team, we ask whether we all think enough at work about the impact of our actions on our health, safety and wellbeing and that of our team.</p>

<h3>Suggested questions</h3>
    <ul>
    <li>Q: What are the health, safety and wellbeing risks you need to consider in your work and how might you address them?</li>
    <li>Q: How can you help ensure that your team understands those risks and how to control them on a day-to-day basis?</li>
    <li>Q: How can you practise your emergency plans so that everyone is aware of what they should do?</li>
    </ul>
    
